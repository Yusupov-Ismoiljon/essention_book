function showSection(id){
    document.querySelectorAll(".section").forEach(sec => sec.style.display = "none");
    document.getElementById(id).style.display = "block";

    if(id === "yodlash") document.getElementById("units").style.display = "grid";
}

// ===================== So‘z o‘yini =====================
let currentWordIndex = 0;
let gameWords = [];

function startGameUnit(unitNumber){
    const unit = units[unitNumber];
    if(!unit) { alert("Unit topilmadi!"); return; }

    gameWords = [...unit];
    currentWordIndex = 0;

    document.getElementById("game-units").style.display = "none";
    document.getElementById("soz-oyini").style.display = "block";
    document.querySelector(".eng-javov").style.display = "block";

    showGameWord();
}

function showGameWord(){
    if(currentWordIndex >= gameWords.length) currentWordIndex = 0;
    document.getElementById("game-word").textContent = gameWords[currentWordIndex].uzb; // so‘rashi ozbekcha
    document.getElementById("game-input").value = "";
    document.getElementById("game-result").textContent = "";
}

function checkAnswer(){
    const input = document.getElementById("game-input").value.trim().toLowerCase();
    const correct = gameWords[currentWordIndex].eng.toLowerCase(); // javob inglizcha

    if(input === correct){
        document.getElementById("game-result").textContent = "✅ To‘g‘ri!";
    } else {
        document.getElementById("game-result").textContent = `❌ Noto‘g‘ri! To‘g‘ri javob: ${gameWords[currentWordIndex].eng}`;
    }
}

function nextGameWord(){
    currentWordIndex++;
    if(currentWordIndex >= gameWords.length) currentWordIndex = 0;
    showGameWord();
}

function exitGame(){
    document.getElementById("soz-oyini").style.display = "none";
    document.getElementById("game-units").style.display = "grid";
    document.querySelector(".eng-javov").style.display = "none";
}