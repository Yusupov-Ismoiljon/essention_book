let wordInterval;
let countdownInterval;

function startUnit(unitNumber) {
    const unit = units[unitNumber];
    if (!unit) { alert("Unit topilmadi!"); return; }

    document.getElementById("units").style.display = "none";
    document.getElementById("yodlash").style.display = "block";

    // So‘zlar qatori ko‘rinadi
    const wordSection = document.getElementById("word-section");
    wordSection.style.display = "flex";
    wordSection.style.justifyContent = "center";
    wordSection.style.gap = "15px";
    wordSection.style.fontSize = "18px";

    const shuffled = [...unit].sort(() => Math.random() - 0.5);
    const wordsToShow = shuffled.slice(0, 20);

    let index = 0;
    const duration = 3000; // har 3 sekundda so‘z o‘zgaradi
    let timer = 100; // 100 sekunddan boshlash

    // avvalgi intervalni tozalash
    if(wordInterval) clearInterval(wordInterval);
    if(countdownInterval) clearInterval(countdownInterval);

    showWord(wordsToShow[index]);
    document.getElementById("timer").textContent = `${timer} s`;

    // So‘zlarni aylantirish
    wordInterval = setInterval(() => {
        index++;
        if(index >= wordsToShow.length){
            clearInterval(wordInterval);
            clearInterval(countdownInterval);
            goBackToMain();
            return;
        }
        showWord(wordsToShow[index]);
    }, duration);

    // Sekundlarni kamaytirish
    countdownInterval = setInterval(() => {
        timer--;
        if(timer < 0){
            clearInterval(countdownInterval);
            clearInterval(wordInterval);
            goBackToMain();
            return;
        }
        document.getElementById("timer").textContent = `${timer} s`;
    }, 1000);
}

function showWord(word){
    document.getElementById("word-display").textContent = word.eng;
    document.getElementById("pronunciation").textContent = word.ipa;
    document.getElementById("translation").textContent = word.uzb;
}

function goBackToMain(){
    document.getElementById("units").style.display = "grid";
    document.getElementById("word-section").style.display = "none";
    document.getElementById("word-display").textContent = "";
    document.getElementById("pronunciation").textContent = "";
    document.getElementById("translation").textContent = "";
    document.getElementById("timer").textContent = "";
    if(wordInterval) clearInterval(wordInterval);
    if(countdownInterval) clearInterval(countdownInterval);
}

